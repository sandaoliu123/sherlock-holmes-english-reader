function processTextWithWords(text) {
  const words = text.split(/(\s+|[.,!?;:'"()-])/g);
  let result = '';
  words.forEach(word => {
    const cleanWord = word.toLowerCase().replace(/[^a-zA-Z]/g, '');
    if (cleanWord && cleanWord.length > 2) {
      result += `<span class="word" data-word="${cleanWord}">${word}</span>`;
    } else {
      result += word;
    }
  });
  return result;
}

