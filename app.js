function addToVocabulary(word, definition) {
  const cleanWord = word.toLowerCase();
  if (vocabularyBook[cleanWord]) {
    return false;
  }
  
  const now = Date.now();
  vocabularyBook[cleanWord] = {
    word: word,
    definition: definition,
    addedAt: now,
    nextReview: now,
    interval: 0,
    reviewCount: 0,
    level: 1
  };
  
  saveVocabularyBook();
  return true;
}

function saveVocabularyBook() {
  localStorage.setItem('vocabularyBook', JSON.stringify(vocabularyBook));
}

function getWordsDueForReview() {
  const now = Date.now();
  return Object.values(vocabularyBook).filter(item => item.nextReview <= now);
}

