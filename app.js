function showWordInfo(word) {
  const wordPanelOverlay = document.getElementById('wordPanelOverlay');
  const wordInfo = document.getElementById('wordInfo');
  const lowerWord = word.toLowerCase();
  const cleanWord = removePunctuationAndSuffixes(word);
  let entry = dictionary[word] || dictionary[lowerWord] || dictionary[cleanWord];
  
  let definition = '';
  
  if (entry) {
    wordInfo.innerHTML = `
      <div class="word">${processTextWithWords(entry.word)}</div>
      <div class="part-of-speech">${entry.partOfSpeech}</div>
      <div class="definition">${processDefinitionWithWords(entry.definition)}</div>
      <div class="example">"${processDefinitionWithWords(entry.example)}"</div>
    `;
    definition = entry.definition;
  } else {
    let basicDef = basicWordDefinitions[lowerWord] || basicWordDefinitions[cleanWord];
    if (basicDef) {
      wordInfo.innerHTML = `
        <div class="word">${processTextWithWords(word)}</div>
        <div class="definition">${processDefinitionWithWords(basicDef)}</div>
      `;
      definition = basicDef;
    } else {
      const smartDef = generateSmartDefinition(word);
      wordInfo.innerHTML = `
        <div class="word">${processTextWithWords(word)}</div>
        <div class="definition">${processDefinitionWithWords(smartDef)}</div>
      `;
      definition = smartDef;
    }
  }
  
  const translation = getTranslation(word);
  if (translation) {
    wordInfo.innerHTML += `
      <div class="definition" style="margin-top: 10px; color: var(--accent-secondary);">
        中文翻译: ${translation}
      </div>
    `;
  }
  
  addToVocabulary(word, definition);
  
  wordPanelOverlay.classList.remove('hidden');
}

