function handleReview(difficulty) {
  const item = currentReviewWords[currentReviewIndex];
  const cleanWord = item.word.toLowerCase();
  
  let intervalIndex = item.interval;
  
  if (difficulty === 'hard') {
    intervalIndex = Math.max(0, intervalIndex - 1);
  } else if (difficulty === 'good') {
    intervalIndex = Math.min(intervalIndex + 1, ebbinghausIntervals.length - 1);
  } else if (difficulty === 'easy') {
    intervalIndex = Math.min(intervalIndex + 2, ebbinghausIntervals.length - 1);
  }
  
  const interval = ebbinghausIntervals[intervalIndex];
  const now = Date.now();
  const nextReview = now + (interval.minutes * 60 * 1000);
  
  vocabularyBook[cleanWord] = {
    ...item,
    interval: intervalIndex,
    level: intervalIndex + 1,
    nextReview: nextReview,
    reviewCount: item.reviewCount + 1
  };
  
  saveVocabularyBook();
  currentReviewIndex++;
  
  if (currentReviewIndex < currentReviewWords.length) {
    showReviewCard();
  } else {
    alert('Review complete! Great job! 🎉');
    updateVocabularyStats();
    renderVocabularyList(Object.values(vocabularyBook));
  }
}